    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

    try {
        // Leer valores ingresados en los textFields (ahora como double)
        double dineroActual = Double.parseDouble(textFieldDineroActual.getText());
        double porcentajeBeneficio = Double.parseDouble(textFieldBeneficio.getText());
        double particion = Double.parseDouble(textFieldParticion.getText());
        

        // Verificar que partici√≥n no sea 0 para evitar divisi√≥n por cero
        if (particion == 0) {
            System.out.println("Error: La partici√≥n no puede ser 0.");
            return;
        }

        // Calcular dinero apostado
        double dineroApostado = dineroActual / particion;

        // Calcular la ganancia generada (multiplicar por el porcentaje directamente)
        double dineroGenerado = dineroApostado * porcentajeBeneficio;

        // Calcular el nuevo total de dinero
        double dineroNuevo = dineroActual + dineroGenerado;

        // Calcular el beneficio final (ganancia neta)
        double beneficioFinal = dineroGenerado - dineroApostado;

        // Mostrar los resultados con 2 decimales
        textFieldApuesta.setText(String.format("%.2f", dineroGenerado));
        jTextField1.setText(String.format("%.2f", dineroNuevo));
        textFieldBeneficioGanado.setText(String.format("%.2f", beneficioFinal));
        textFieldCuantoApostar.setText(String.format("%.2f", dineroApostado));
        
        textFieldDineroActual.setText("");
        textFieldBeneficio.setText("");
        textFieldParticion.setText("");
        
        double dineroActualPerdida = dineroActual - dineroApostado;
        
        jTextArea1.setText("El analisis es el siguiente: \n "
                          +"Ganaras " +dineroGenerado+"‚Ç¨ \n"
                          +"Teniendo un total de "+dineroNuevo+"‚Ç¨ \n"
                          +"Si pierdes acabarias con "+dineroActualPerdida+"‚Ç¨");
        
        

    } catch (NumberFormatException e) {
        System.out.println("Error: Ingresa solo n√∫meros v√°lidos en los campos.");
    }
    }//GEN-LAST:event_jButton1ActionPerformed

